/* Functions in SQL */

/* Average function */
SELECT AVG(age) FROM Students1;

/* count function */
SELECT COUNT(age) FROM Students1;

/* MAX  function */
SELECT MAX(AGE) FROM Students1

/* Min function */
SELECT MIN(AGE) FROM Students1

/* SUM FUNCTION */
SELECT SUM(AGE) FROM Students1

/* ABS FUNCTION */
SELECT ABS(AGE) FROM Students1
WHERE NAME='ISHA';

/* LENGTH FUNCTION */
SELECT LENGTH (AGE) FROM Students1;  

/* UCASE FUNCTION */
SELECT UCASE(NAME) FROM Students1;

/* LCASE FUNCTION */
SELECT LCASE(NAME)FROM Students1;

/* ROUND FUNCTION */
SELECT ROUND(AGE) FROM Students1